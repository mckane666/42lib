int	ft_isalpha(int c)
{
	int re;
	unsigned char l;

	l = c;
	if ((l >= 'a' && l <= 'z') ||
				(l >= 'A' && l <= 'Z'))
	{
		re = 1;
	}
	else
	{
	    re = 0;
	}
	return (re);
}
