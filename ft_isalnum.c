int	ft_isalnum(int c)
{
	int				re;
	unsigned char	l;

	l = c;
	if ((l >= 'a' && l <= 'z') ||
				(l >= 'A' && l <= 'Z') || (l >= '0' && l <= '9'))
	{
		re = 1;
	}
	else
	{
		re = 0;
	}
	return (re);
}
