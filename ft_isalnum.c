int	ft_isalnum(int c)
{
	int	re;
	if ((c >= 'a' && c <= 'z') ||
				(c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9'))
	{
		re = 1;
	}
	else
	{
		re = 0;
	}
	return (re);
}
